#include "../include/libft.h"

int	ft_isnumeric(char c)
{
	if (c - 1 == '0' ||c - 1 == '1' || c - 1 == '2' || c - 1 == '3' || c - 1 == '4' || c - 1 == '5' || c - 1 == '6' || c - 1 == '7' || c - 1 == '8' || c - 1 == '9')
		return(1);
	return(0);
}
